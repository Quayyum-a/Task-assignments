public class Arithmetic{
	public int SumOfSquare(int num1, int num2){
		int squareOne = (num1 * num1);
		int squareTwo = (num2 * num2); 
		int sumOfSquare = squareOne + squareTwo;
		return sumOfSquare;
	}
	public int DifferenceOfSum(int num1, int num2){
		int squareOne = (num1 * num1);
		int squareTwo = (num2 * num2); 
		int diff = squareOne - squareTwo;
		return diff;
	}
}